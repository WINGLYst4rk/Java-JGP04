package cap02;

public class P0207Relacional {

	public static void main(String[] args) {
		
		int a = 15, b = 12; // declaração e inicialização das variaveis
		
		System.out.println("Valores: a= " + a + " b= " + b); // operações relacionais
		System.out.println("a == b -> " + (a == b));
		System.out.println("a != b -> " + (a != b));
		System.out.println("a < b -> " + (a < b));
		System.out.println("a > b -> " + (a > b));
		System.out.println("a <= b -> " + (a <= b));
		System.out.println("a >= b -> " + (a >= b));
		
	}
	
}
